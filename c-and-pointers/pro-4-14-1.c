#include <stdio.h>
#include <assert.h>

double square_root(double n)
{
    assert(n > 0);
    double pre, cur, tmp;
    for (pre = 0, cur = 1; cur != pre; )
    {
        tmp = cur;
        cur = (cur + n / cur) / 2;
        pre = tmp;
    }
    return cur;
}

int main()
{
    int number;
    for (number = 1; number < 100; ++number)
    {
        double sr = square_root((double) number);
        printf("%.6f %.6f\n", sr, sr * sr);
    }
    return 0;
}

